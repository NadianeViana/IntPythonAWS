#Calcular a área de uma circunferência após receber o valor do raio.
raioCirc = float(input("Informe o valor do raio: "))

#Execução do cálculo da área

raioTotal = raioCirc * raioCirc
#Ou poderia usar area = 34.14 * (raioCirc ** 2)
areaTotal = 3.14 * raioTotal


#Exibição da Área

print("A área da circunferência é: ", areaTotal)
